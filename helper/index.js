function average(x, y) {
  return (x + y) / 2;
}

function isEven(x) {
  return x % 2 === 0;
}

function square(x) {
  return x * x;
}

function increase(x) {
  return x + 1;
}

function plus(a, b) {
  return a + b;
}

function minus(a, b) {
  return a - b;
}

function divide(a, b) {
  return a / b;
}

function multiply(a, b) {
  return a * b;
}

exports.average = average;
exports.isEven = isEven;
exports.square = square;
exports.increase = increase;
exports.plus = plus;
exports.minus = minus;
exports.divide = divide;
exports.multiply = multiply;
